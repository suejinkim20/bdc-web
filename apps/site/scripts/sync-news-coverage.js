/**
 * scripts/sync-news-coverage.js
 *
 * Fetches the news coverage tab from a private Google Sheet via
 * @bdc/google-api, validates and cleans each row, and writes
 * the result as YAML to content/news-coverage.yaml.
 *
 * Usage:
 *   node --env-file=../../.env scripts/sync-news-coverage.js
 *
 * Required env vars:
 *   SHEET_ID                  — Google Sheet ID
 *   NEWS_COVERAGE_TAB         — sheet tab name (e.g. "News Coverage")
 *   GOOGLE_CREDENTIALS_BASE64 — base64-encoded service account key JSON
 */

import fs from "fs";
import path from "path";
import yaml from "js-yaml";
import { fetchGoogleSheetValues } from "@bdc/google-api";

// ---------------------------------------------------------------------------
// Config
// ---------------------------------------------------------------------------

const SHEET_ID = process.env.SHEET_ID;
const TAB_NAME = process.env.NEWS_COVERAGE_TAB;
const OUT_PATH = path.resolve("src/content/coverage.yaml");

const REQUIRED_COLUMNS = ["title", "date", "source", "url", "external", "paywall"];
const REQUIRED_FIELDS  = ["title", "date", "source", "url", "external"];

if (!SHEET_ID) {
  console.error("Error: SHEET_ID environment variable is required.");
  process.exit(1);
}

if (!TAB_NAME) {
  console.error("Error: NEWS_COVERAGE_TAB environment variable is required.");
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Validation
// ---------------------------------------------------------------------------

/** Returns an array of human-readable error strings for a single row. */
function validateRow(row, index) {
  const errors = [];
  const rowNum = index + 2;

  // Required fields
  for (const field of REQUIRED_FIELDS) {
    if (!row[field]?.trim()) {
      errors.push(`Row ${rowNum}: missing required field "${field}"`);
    }
  }

  // Date format
  if (row.date && !/^\d{4}-\d{2}-\d{2}$/.test(row.date.trim())) {
    errors.push(`Row ${rowNum}: "date" should be YYYY-MM-DD, got "${row.date}"`);
  }

  // URL format
  if (row.url?.trim()) {
    try { new URL(row.url.trim()); }
    catch { errors.push(`Row ${rowNum}: invalid URL "${row.url}"`); }
  }

  // Boolean fields
  const validBooleans = ["yes", "no", "true", "false"];
  if (row.external && !validBooleans.includes(row.external.trim().toLowerCase())) {
    errors.push(`Row ${rowNum}: "external" should be yes/no, got "${row.external}"`);
  }
  if (row.paywall && !validBooleans.includes(row.paywall.trim().toLowerCase())) {
    errors.push(`Row ${rowNum}: "paywall" should be yes/no, got "${row.paywall}"`);
  }

  return errors;
}

// ---------------------------------------------------------------------------
// Boolean normalizer
// ---------------------------------------------------------------------------

/** Converts yes/no/true/false strings to a boolean. */
function toBoolean(val) {
  return ["yes", "true"].includes((val ?? "").trim().toLowerCase());
}

// ---------------------------------------------------------------------------
// Fetch — returns rows as array of objects
// ---------------------------------------------------------------------------

async function fetchSheetRows() {
  console.log(`Fetching news coverage from tab "${TAB_NAME}"…`);

  const rows = await fetchGoogleSheetValues(SHEET_ID, TAB_NAME);

  if (rows.length < 2) throw new Error("Sheet appears to be empty.");

  const [headers, ...body] = rows;
  return body.map((row) =>
    Object.fromEntries(headers.map((h, i) => [h.trim().toLowerCase(), (row[i] ?? "").trim()]))
  );
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

async function main() {
  // 1. Fetch
  const data = await fetchSheetRows();

  // 2. Check required columns exist
  const missingCols = REQUIRED_COLUMNS.filter((c) => !(c in (data[0] ?? {})));
  if (missingCols.length) {
    throw new Error(`Sheet is missing required columns: ${missingCols.join(", ")}`);
  }

  console.log(`Fetched ${data.length} rows.`);

  // 3. Validate
  const validationErrors = data.flatMap((row, i) => validateRow(row, i));
  if (validationErrors.length) {
    console.warn(`\n⚠️  Validation warnings (${validationErrors.length}):`);
    validationErrors.forEach((e) => console.warn("  •", e));
    console.warn("");
  }

  // 4. Clean and normalize
  const items = data.map((row) => ({
    title:    row.title,
    date:     row.date,
    source:   row.source,
    url:      row.url,
    external: toBoolean(row.external),
    paywall:  toBoolean(row.paywall),
  }));

  // 5. Serialize to YAML
  const yamlOut = [
    "# content/news-coverage.yaml",
    "# Auto-generated by scripts/sync-news-coverage.js — do not edit manually.",
    `# Last synced: ${new Date().toISOString()}`,
    "#",
    yaml.dump(items, { lineWidth: 120, quotingType: '"' }),
  ].join("\n");

  // 6. Write
  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, yamlOut, "utf8");

  console.log(`✓ Written ${items.length} items to ${OUT_PATH}`);

  if (validationErrors.length) process.exit(2);
}

main().catch((err) => {
  console.error("Fatal:", err.message);
  process.exit(1);
});